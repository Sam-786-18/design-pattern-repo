package com.mhb.pattern.vo;

public class CompleteStockDetailsVO {
private String stockName;
private String country;
private String stockId;
private String stockPrice;
private String stockFuturePrice;
private String exchangeName;
public String getStockName() {
	return stockName;
}
public void setStockName(String stockName) {
	this.stockName = stockName;
}
public String getCountry() {
	return country;
}
public void setCountry(String country) {
	this.country = country;
}
public String getStockId() {
	return stockId;
}
public void setStockId(String stockId) {
	this.stockId = stockId;
}
public String getStockPrice() {
	return stockPrice;
}
public void setStockPrice(String stockPrice) {
	this.stockPrice = stockPrice;
}
public String getStockFuturePrice() {
	return stockFuturePrice;
}
public void setStockFuturePrice(String stockFuturePrice) {
	this.stockFuturePrice = stockFuturePrice;
}
public String getExchangeName() {
	return exchangeName;
}
public void setExchangeName(String exchangeName) {
	this.exchangeName = exchangeName;
}


}
